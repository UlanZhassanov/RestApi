CREATE TABLE users (
  id BIGINT(20) NOT NULL AUTO_INCREMENT,
  name VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE events (
  id BIGINT(20) NOT NULL AUTO_INCREMENT,
  user_id BIGINT(20) DEFAULT NULL,
  file_id BIGINT(20) DEFAULT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE files (
  id BIGINT(20) NOT NULL AUTO_INCREMENT,
  name VARCHAR(50) DEFAULT NULL,
  file_path VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (id)
);

ALTER TABLE events
  ADD CONSTRAINT fk_user_event FOREIGN KEY (user_id)
    REFERENCES users(id);

ALTER TABLE events
  ADD CONSTRAINT fk_file_event FOREIGN KEY (file_id)
    REFERENCES files(id);